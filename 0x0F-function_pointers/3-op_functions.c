#include "3-calc.h"

/**
 * op_add - sum of two integer
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sudstract two integer
 * @a: first integer
 * @b: second integer
 * Return: difference of two integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two integer
 * @a: first integer
 * @b: second integer
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two integer
 * @a: first integer
 * @b: second integer
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of a divided b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
